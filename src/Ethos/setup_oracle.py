def get_connection():
    try:
        host = os.getenv('ORACLE_HOST')
        port = os.getenv('ORACLE_PORT')
        service = os.getenv('ORACLE_SERVICE_NAME')
        user = os.getenv('ORACLE_USER')
        
        print(f"Connecting to Oracle at {host}:{port}/{service} as {user}...")
        
        dsn = f"{host}:{port}/{service}"
        conn = oracledb.connect(
            user=user,
            password=os.getenv("ORACLE_PASSWORD"),
            dsn=dsn
        )
        print("Connected to Oracle DB successfully.")
        return conn
    except Exception as e:
        print(f"Connection failed: {e}")
        return None

def execute_sql(cursor, sql, params=None):
    try:
        if params:
            cursor.execute(sql, params)
        else:
            cursor.execute(sql)
        # Verify execution by printing start of SQL
        print(f"Executed: {sql.strip()[:50]}...")
    except oracledb.DatabaseError as e:
        error, = e.args
        # Ignore "table or view does not exist" errors during drop (ORA-00942)
        if error.code != 942:
            print(f"Error executing SQL: {e}")

